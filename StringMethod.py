user_input = input("Please provide input: ")
# Length of input
print(f"Length: {len(user_input)}")
# Find first occurrence
print(f"First occurrence of o: {user_input.find('o')}")
# Find last occurrence
print(f"Last occurrence of o: {user_input.rfind('o')}")
# Capitalize first letter
print(f"Capitalized: {user_input.capitalize()}")
# Make upper cased
print(f"Uppercase: {user_input.upper()}")
# Make lower cased
print(f"Lowercase: {user_input.lower()}")


